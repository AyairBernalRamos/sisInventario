    private void listarClientes(){
        List<clientes> lista = auClientes.Listar();
        modeloCliente = (DefaultTableModel) tbclientes.getModel();
        Object[] ob = new Object[7];
        for(int i=0;i<lista.size();i++){
            ob[0]=lista.get(i).getIdCliente();
            ob[1]=lista.get(i).getNombres();
            ob[2]=lista.get(i).getApellidos();
            ob[3]=lista.get(i).getDocumento();
            ob[4]=lista.get(i).getDireccion();
            ob[5]=lista.get(i).getTelefono();
            ob[6]=lista.get(i).getCorreo();
            modeloCliente.addRow(ob);
            
            
        }
        tbclientes.setModel(modeloCliente);
    }
    


    void limpiarCampos(){
        txtidcliente.setText("");
        txtnombre.setText("");
        txtapellido.setText("");
        txtdocumento.setText("");
        txtdireccion.setText("");
        txttelefono.setText("");
        txtcorreo.setText("");
    }
    void limpiarTablaClientes(){
        for(int i=0;i<modeloCliente.getRowCount();i++){
            modeloCliente.removeRow(i);
            i=0-1;
        }
    }
    

